#include<iostream>
using namespace std;

const double PI = 3.1415926;

//设计一个圆类，来求元的周长
class Circle
{
	//访问权限
public:
	//属性
	int m_r;

	//行为：获取圆的周长
	double calculateZC()
	{
		return 2 * PI * m_r;
	}
};



int main()
{
	//实例化：通过圆类创建一个具体的圆（对象）
	Circle c1;
	//给圆对象的属性进行赋值
	c1.m_r = 10;
	//输出圆的周长
	cout << "圆的周长为：" << c1.calculateZC() << endl;

	system("pause");
	return 0;
}